package Programs;

public class InstanceVariable {
	static String a="Hello";
	static int i;
	static float b=21.15f;
	static double db=21032002;
	static boolean istrue=true;
	static char c1='H';
	public static void main(String args[]) {
		System.out.println("The value of string is "+ a);
		System.out.println("The value of int is "+ i);
		System.out.println("The value of float is "+ b);
		System.out.println("The value of double is "+ db);
		System.out.println("The value of boolean is "+ istrue);
		System.out.println("The value of char is "+ c1);
	}
}
